
function split (inputstr, sep)
        if sep == nil then
                sep = "%s"
        end
        local t={}
        for str in string.gmatch(inputstr, "([^"..sep.."]+)") do
                table.insert(t, str)
        end
        return t
end

function AsciiToLines(asciiArt)
    local split = split(asciiArt,"\n")
    return split
end

